#include "libft.h"

void		ft_strclr(char *s)
{
	int x;
	int y;

	if (s != NULL)
	{
		x = 0;
		y = 0;
		while (s[x] != '\0')
			x++;
		while (y < x)
		{
			*s = '\0';
			s++;
			y++;
		}
	}
}
