#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int x;
	int y;

	y = 0;
	x = 0;
	if (s != NULL && f != NULL)
	{
		while (s[x] != '\0')
			x++;
		while (y < x)
		{
			(*f)(s);
			s++;
			y++;
		}
	}
}
