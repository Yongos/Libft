#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	int x;

	x = 0;
	if (s1 && s2)
	{
		while (s1[x] == s2[x])
		{
			if (s1[x] == 0 && s2[x] == 0)
				return (1);
			x++;
		}
		return (0);
	}
	return (8);
}
